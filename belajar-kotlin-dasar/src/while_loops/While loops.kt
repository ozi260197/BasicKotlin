fun main() {

    // Kode While
    var i = 0
    while(i < 10) {
        println("Perulangan $i")
        i++
    }
    println("Selesai")

}

// While adalah salah satu perulangan lain yang ada di kotlin
// While adalah salah satu perulangan yang sangat flexibel, dimana kode while akan melakukan pengecekan kondisi,
// Jika kondisi bernilai true, Maka dia akan menjalankan blok while, dan terus diulangi sampai kondisi while bernilai false