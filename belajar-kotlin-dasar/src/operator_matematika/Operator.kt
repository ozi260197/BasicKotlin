fun main() {

    // Kode operasi matematika
    var result1: Int = 10 / 3
    println(result1)
    var result2: Int = 10 + 10 / 2
    println(result2)

    // Contoh Menggunakan operasi matematika
    var total: Int = 0

    val barang1 = 100
    total = total + barang1

    val barang2 = 200
    total = total + barang2

    val barang3 = 300
    total = total + barang3

    println(total)

    // Contoh menggunakan Augmented Assignments
    val barang4 = 400
    total += barang4

    val barang5 = 500
    total += barang5

    val barang6 = 600
    total += barang6

    println(total)

    // Contoh menggunakan Unary Operator
    total++         // Kenaikan Satu
    println(total)

    total--         // Pengurangan Satu
    println(total)

    val suhu = -5
    println(suhu)

    val suhu1 = +5 // Default
    println(suhu1)

    val sehat = true
    println(sehat)

    println(!sehat) // Boolean Kebalikan

}

