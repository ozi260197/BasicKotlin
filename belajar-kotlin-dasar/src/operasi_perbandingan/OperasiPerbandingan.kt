fun main() {

    // Kode Opeartor perbandingan
    val a = 100
    val b = 200

    val result: Boolean = a > b
    println(result)

    val result1: Boolean = a < b
    println(result1)

    val result2: Boolean = a >= b
    println(result2)

    val result3: Boolean = a <= b
    println(result3)

    val result4: Boolean = a == b
    println(result4)

    val result5: Boolean = a != b
    println(result5)

    println("a" < "b")
    println("a" == "a")
    println("a" != "b")

}

// Operasi perbandingan adalah operasi untuk membandingkan dua buah data
// Operasi perbandingan adalah operasi yang menghasilkan nilai boolean (Benar atau Salah) / (True or False)
// Jika hasil operasi nya adalah benar, Maka nilai nya adalah True
// Jika hasil operasi nya adalah salah, Maka nilai nya adalah False

// Todo: Opeartor Perbandingan
// >       ->   Lebih Dari
// <       ->   Kurang Dari
// >=      ->   Lebih Dari atau Samadengan
// <=      ->   Kurang Dari atau Samadengan
// ==      ->   Samadengan
// !=      ->   Tidak Samadengan